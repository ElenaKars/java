package lesson_03;

public class Variables {
    public static void main(String[] args) {
        int myFirstVariable;
        myFirstVariable = 1;

        int mySecondVariable = 25;

        System.out.println(mySecondVariable);

        mySecondVariable = 50;

        System.out.println(mySecondVariable);

        long longVar = 8_230_000_000_000_000_000L;

        System.out.println("longVar: " + longVar);

    }
}
